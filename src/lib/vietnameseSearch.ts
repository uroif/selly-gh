function createVietnameseSearchPattern(searchQuery: string): string {
  const lowerQuery = searchQuery.toLowerCase()
  let pattern = ''
  
  for (const char of lowerQuery) {
    const variations = vietnameseCharMap[char]
    if (variations && variations.length > 1) {
      
      pattern += `[${variations.join('')}]`
    } else {
      
      const escapedChar = char.replace(/[.*+?^${}()|[\]\\]/g, '\\$&')
      pattern += escapedChar
    }
  }
  
  return pattern
}


export function vietnameseMatch(text: string, searchQuery: string): boolean {
  if (!searchQuery.trim()) return true
  if (!text) return false
  
  
  
  let effectiveQuery = searchQuery
  if (/^\d+$/.test(searchQuery) && searchQuery.startsWith('0')) {
    const normalizedQuery = searchQuery.replace(/^0+/, '')
    
    effectiveQuery = normalizedQuery || '0'
  }
  
  
  const pattern = createVietnameseSearchPattern(effectiveQuery)
  const regex = new RegExp(pattern, 'i')
  
  return regex.test(text)
}


export function vietnameseFilter<T>(
  items: T[],
  searchQuery: string,
  textExtractor: (item: T) => string | string[]
): T[] {
  if (!searchQuery.trim()) return items
  
  return items.filter((item) => {
    const texts = textExtractor(item)
    const textsArray = Array.isArray(texts) ? texts : [texts]
    
    return textsArray.some((text) => vietnameseMatch(text, searchQuery))
  })
}
